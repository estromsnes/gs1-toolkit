package no.nofuzz.gs1.ai;

public enum CharacterSet {
    /**
     * Numeric characters only (0-9)
     */
    NUMERIC,

    /**
     * Alphanumeric characters (0-9, A-Z, and limited special characters)
     */
    ALPHANUMERIC,

    /**
     * Any characters allowed (no restriction)
     */
    ANY
}
